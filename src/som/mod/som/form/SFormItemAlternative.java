    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbItem) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moTextName.setValue(moRegistry.getName());
        moTextCode.setValue(moRegistry.getCode());
        
        moBoolYieldPercentage.setValue(moRegistry.isAlternativeYieldPercentage());
        moBoolMoisiturePercentage.setValue(moRegistry.isAlternativeMoisiturePercentage());
        moBoolAcidityPercentage.setValue(moRegistry.isAlternativeAcidityPercentage());

        setFormEditable(true);

        if (moRegistry.isRegistryNew()) {

        }
        
        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbItem registry = moRegistry.clone();

        if (registry.isRegistryNew()) {
            
        }

        registry.setAlternativeYieldPercentage(moBoolYieldPercentage.getValue());
        registry.setAlternativeMoisiturePercentage(moBoolMoisiturePercentage.getValue());
        registry.setAlternativeAcidityPercentage(moBoolAcidityPercentage.getValue());

        return registry;
    }

