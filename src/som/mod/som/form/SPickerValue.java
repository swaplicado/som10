    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton) {
            JButton button = (JButton) e.getSource();
            
            if (button == jbCopyValueText) {
                actionPerformedCopyValueText(true);
            }
            else if (button == jbClearSearchText) {
                actionPerformedClearSearchText();
            }
        }
        else if (e.getSource() instanceof JToggleButton) {
            JToggleButton toggleButton = (JToggleButton) e.getSource();
            
            if (toggleButton == jtbShowElegible || toggleButton == jtbShowNew || toggleButton == jtbShowAll) {
                actionPerformedShow();
            }
        }
    }

