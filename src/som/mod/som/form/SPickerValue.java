    @Override
    public void reloadCatalogues() {
        try {
            maAllValues = moField.retriveValues(miClient.getSession());
            maNewValues = new ArrayList<>();
            maElegibleValues = new ArrayList<>();
            
            for (SDbValue value : maAllValues) {
                if (value.getScopeInputCategories().isEmpty() && value.getScopeItems().isEmpty()) {
                    maNewValues.add(value);
                }
            }
            
            for (SDbValue value : maAllValues) {
                if (value.getCompScopeInputCategoriesSet().contains(moTicketItem.getFkInputCategoryId()) || value.getCompScopeItemsSet().contains(moTicketItem.getPkItemId())) {
                    maElegibleValues.add(value);
                }
            }
        }
        catch (Exception e) {
            SLibUtils.showException(this, e);
        }
    }

