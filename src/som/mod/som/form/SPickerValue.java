    private void initComponentsCustom() {
        SGuiUtils.setWindowBounds(this, 800, 500);
        
        moTextSearchText.setTextSettings(SGuiUtils.getLabelName(jlSearchText), 100, 0);
        
        moFields.addField(moTextSearchText);
        moFields.setFormButton(jbSave);
        
        moField = (SDbField) miClient.getSession().readRegistry(SModConsts.C_FIELD, new int[] { mnFormSubtype });
        String fieldName = SLibUtils.textFirstUpperCase(moField.getName());
        
        ((TitledBorder) jpMain.getBorder()).setTitle(((TitledBorder) jpMain.getBorder()).getTitle().replace("<field>", moField.getName().toLowerCase()));
        jlValueText.setText(jlValueText.getText().replace("<field>", fieldName));
        
        moValuesGrid = new SGridPaneForm(miClient, mnFormType, mnFormSubtype, true, fieldName) {
            
            @Override
            public void initGrid() {
                setRowButtonsEnabled(false);
            }
            
            @Override
            public void createGridColumns() {
                int col = 0;
                SGridColumnForm[] columns = new SGridColumnForm[4];

                columns[col++] = new SGridColumnForm(SGridConsts.COL_TYPE_TEXT_NAME_CAT_M, fieldName);
                columns[col++] = new SGridColumnForm(SGridConsts.COL_TYPE_TEXT_NAME_CAT_S, "Referencia");
                columns[col++] = new SGridColumnForm(SGridConsts.COL_TYPE_TEXT_NAME_CAT_M, "Aplica categorías insumo");
                columns[col++] = new SGridColumnForm(SGridConsts.COL_TYPE_TEXT_NAME_CAT_L, "Aplica ítems");

                for (col = 0; col < columns.length; col++) {
                    moModel.getGridColumns().add(columns[col]);
                }
            }
        };
        
        moValuesGrid.setPaneFormOwner(this);
        //mvFormGrids.add(moValuesGrid);

        jpValues.add(moValuesGrid, BorderLayout.CENTER);
        
        jlValuesShown = new JLabel();
        jlValuesShown.setPreferredSize(new Dimension(200, 23));
        jpCommandLeft.add(jlValuesShown);
        
        // default settings:
        
        moRadSearchExact.setSelected(true);
        jtbShowElegible.setSelected(true);
    }
    
    private void renderTicketItem() {
        if (moTicketItem == null) {
            jtfTicketItem.setText("");
            jtfTicketInputCategory.setText("");
        }
        else {
            jtfTicketItem.setText(moTicketItem.getName());
            jtfTicketInputCategory.setText((String) miClient.getSession().readField(SModConsts.SU_INP_CT, new int[] { moTicketItem.getFkInputCategoryId() }, SDbRegistry.FIELD_NAME));
            
            jtfTicketItem.setCaretPosition(0);
            jtfTicketInputCategory.setCaretPosition(0);
        }
    }
    
    private void showValues() {
        moValuesGrid.clearGridRows();
        jlValuesShown.setText("Valores mostrados: 0");
        
        ArrayList<SDbValue> availableValues = null;
        
        if (jtbShowAll.isSelected()) {
            availableValues = maAllValues;
        }
        else if (jtbShowNew.isSelected()) {
            availableValues = maNewValues;
        }
        else if (jtbShowElegible.isSelected()) {
            availableValues = maElegibleValues;
        }
        
        if (availableValues != null && !availableValues.isEmpty()) {
            ArrayList<SDbValue> values = new ArrayList<>();
            String text = moTextSearchText.getValue().toUpperCase();
            
            if (text.isEmpty()) {
                values.addAll(availableValues);
            }
            else {
                if (moRadSearchExact.isSelected()) {
                    for (SDbValue value : availableValues) {
                        if (value.getValueText().startsWith(text)) {
                            values.add(value);
                        }
                    }
                }
                else {
                    for (SDbValue value : availableValues) {
                        if (value.getValueText().contains(text)) {
                            values.add(value);
                        }
                    }
                }
            }
            
            moValuesGrid.populateGrid(new Vector<>(values));
            jlValuesShown.setText("Valores mostrados: " + SLibUtils.DecimalFormatInteger.format(values.size()));
        }
    }
    
    private void actionPerformedCopyValueText(final boolean requestFocus) {
        moTextSearchText.setValue(jtfValueText.getText());
        showValues();
        
        if (requestFocus) {
            moTextSearchText.requestFocusInWindow();
        }
    }
    
    private void actionPerformedClearSearchText() {
        moTextSearchText.resetField();
        showValues();
        
        moTextSearchText.requestFocusInWindow();
    }
    
    private void actionPerformedShow() {
        showValues();
    }
    
