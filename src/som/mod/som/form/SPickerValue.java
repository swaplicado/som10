    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();
        
        if (validation.isValid()) {
            if (moValuesGrid.getSelectedGridRow() == null) {
                validation.setMessage(SGridConsts.MSG_SELECT_ROW);
                validation.setComponent(moValuesGrid.getTable());
            }
        }
        
        return validation;
    }
    
    @Override
    public void setValue(final int type, final Object value) {
        switch (type) {
            case SModConsts.SU_ITEM:
                moTicketItem = (SDbItem) value;
                renderTicketItem();
                break;
            case SModConsts.C_VALUE:
                // value's text:
                jtfValueText.setText((String) value);
                jtfValueText.setCaretPosition(0);
                actionPerformedCopyValueText(false);
                break;
            default:
                // nothing
        }
    }
    
    @Override
    public Object getValue(final int type) {
        Object value = null;
        
        switch (type) {
            case SModConsts.C_VALUE:
                // value's text:
                SGridRow row = moValuesGrid.getSelectedGridRow();
                if (row != null) {
                    value = row.getRowName();
                }
                break;
            default:
                // nothing
        }
        
        return value;
    }

