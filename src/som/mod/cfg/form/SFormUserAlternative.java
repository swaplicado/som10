    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbUser) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            jtfRegistryKey.setText("");
            moRegistry.setFkUserTypeId(SModSysConsts.CS_USR_TP_USR);
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moTextName.setValue(moRegistry.getName());
        

        for (SDbUserRightAlternative right : moRegistry.getChildUserRightsAlternatives()) {
            for (SGridRow row : moGridUserRights.getModel().getGridRows()) {
                if (right.getPkRightId() == ((SDbUserRightAlternative) row).getPkRightId()) {
                    ((SDbUserRightAlternative) row).setXtaSelected(true);
                }
            }
        }

        
        setFormEditable(true);

        if (moRegistry.isRegistryNew()) {
        
        }
                
        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbUser registry = moRegistry.clone();

        if (registry.isRegistryNew()) {}

        //registry.setName(moTextName.getValue());
       
        registry.getChildUserRightsAlternatives().clear();
        for (SGridRow row : moGridUserRights.getModel().getGridRows()) {
            if (((SDbUserRightAlternative) row).isXtaSelected()) {
                registry.getChildUserRightsAlternatives().add((SDbUserRightAlternative) row);
            }
        }

        return registry;
    }

