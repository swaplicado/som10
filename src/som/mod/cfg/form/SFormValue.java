    private void renderField() {
        if (moField == null) {
            jtfFieldDescription.setText("");
            jtfFieldSettings.setText("");
            jtfFieldTrimCharacters.setText("");
            
            jtfFieldDescription.setToolTipText(null);
            jtfFieldSettings.setToolTipText(null);
            jtfFieldTrimCharacters.setToolTipText(null);
            
            jbViewFieldSettings.setEnabled(false);
        }
        else {
            jtfFieldDescription.setText(moField.getDescription());
            jtfFieldSettings.setText(moField.getSettings());
            jtfFieldTrimCharacters.setText(moField.getTrimCharacters());
            
            jtfFieldDescription.setCaretPosition(0);
            jtfFieldSettings.setCaretPosition(0);
            jtfFieldTrimCharacters.setCaretPosition(0);
            
            jtfFieldDescription.setToolTipText(jtfFieldDescription.getText());
            jtfFieldSettings.setToolTipText(jtfFieldSettings.getText());
            jtfFieldTrimCharacters.setToolTipText(jtfFieldTrimCharacters.getText());
            
            jbViewFieldSettings.setEnabled(true);
        }
    }

