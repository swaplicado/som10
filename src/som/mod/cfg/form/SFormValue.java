    private void itemStateChangedField() {
        if (moKeyField.getSelectedIndex() <= 0) {
            moField = null;
        }
        else {
            moField = (SDbField) miClient.getSession().readRegistry(SModConsts.C_FIELD, moKeyField.getValue());
        }
        
        renderField();
    }
    
