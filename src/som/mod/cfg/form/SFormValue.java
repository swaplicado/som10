    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbValue) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            
            moKeyField.setValue(new int[] { mnFormSubtype });
            
            jtfRegistryKey.setText("");
        }
        else {
            moKeyField.setValue(new int[] { moRegistry.getFkFieldId() });
            
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }
        
        itemStateChangedField();

        moTextValueText.setValue(moRegistry.getValueText());
        moTextValueReference.setValue(moRegistry.getValueReference());
        moTextScopeInputCategories.setValue(moRegistry.getScopeInputCategories());
        moTextScopeItems.setValue(moRegistry.getScopeItems());

        setFormEditable(true);
        
        moKeyField.setEnabled(false); // immutable value
        
        if (moRegistry.isRegistryNew()) {
            jbEditValueText.setEnabled(false);
            jbEditScopeInputCategories.setEnabled(false);
            jbEditScopeItems.setEnabled(false);
        }
        else {
            jbEditValueText.setEnabled(true);
            jbEditScopeInputCategories.setEnabled(true);
            jbEditScopeItems.setEnabled(true);
            
            moTextValueText.setEditable(false);
            moTextScopeInputCategories.setEditable(false);
            moTextScopeItems.setEditable(false);
        }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbValue registry = moRegistry.clone();
        
        if (registry.isRegistryNew()) {
            registry.setFkFieldId(moKeyField.getValue()[0]); // immutable value
        }

        registry.setValueText(moTextValueText.getValue());
        registry.setValueReference(moTextValueReference.getValue());
        registry.setScopeInputCategories(moTextScopeInputCategories.getValue());
        registry.setScopeItems(moTextScopeItems.getValue());

        return registry;
    }

    @Override
    public SGuiValidation validateForm() {
        // sanitize data:
        
        SGuiValidation validation = new SGuiValidation();
        
        HashMap<Integer, SBeanFieldText> fields = new HashMap<>(); // key: fieldId; value: text field
        HashMap<Integer, String> sanitizedValues = new HashMap<>(); // key: fieldId; value: sanitized value
        
        if (moTextValueText.isEditable()) {
            fields.put(1, moTextValueText);
            sanitizedValues.put(1, moField.sanitizeValueText(moTextValueText.getValue()));
        }
        if (moTextScopeInputCategories.isEditable() && !moTextScopeInputCategories.getValue().isEmpty()) {
            fields.put(2, moTextScopeInputCategories);
            sanitizedValues.put(2, SDbValue.sanitizeDataIds(moTextScopeInputCategories.getValue()));
        }
        if (moTextScopeItems.isEditable() && !moTextScopeItems.getValue().isEmpty()) {
            fields.put(3, moTextScopeItems);
            sanitizedValues.put(3, SDbValue.sanitizeDataIds(moTextScopeItems.getValue()));
        }
        
        for (Integer fieldId : sanitizedValues.keySet()) {
            SBeanFieldText field = fields.get(fieldId);
            String sanitizedValue = sanitizedValues.get(fieldId);
            
            if (!field.getValue().equals(sanitizedValue)) {
                String action = (sanitizedValue.isEmpty() ? "será borrado totalmente" : "será sustituido por el valor '" + sanitizedValue + "'") + ".";
                
                if (miClient.showMsgBoxConfirm(SGuiConsts.ERR_MSG_FIELD_VAL_ + "'" + field.getFieldName() + "', '" + field.getValue() + "',\n"
                        + "no cumple con el formato requerido, por lo " + action + "\n" + SGuiConsts.MSG_CNF_CONT) == JOptionPane.YES_OPTION) {
                    field.setValue(sanitizedValue);
                }
                else {
                    validation.setMessage(SGuiConsts.ERR_MSG_FIELD_DIF + "'" + field.getFieldName() + "'.");
                    validation.setComponent(field);
                }
            }
        }
        
        // validate data:
        
        if (validation.isValid()) {
            validation = moFields.validateFields();
            JComponent component = null;

            // check text value for no duplicity and formatting rules:

            try {
                if (validation.isValid()) {
                    if (moTextValueText.isEditable()) {
                        component = moTextValueText;

                        // an exception will be thrown on validation failure:
                        moField.validateValueTextFormat(miClient, moTextValueText.getValue());

                        int count = SDbValue.countOccurrences(miClient.getSession(), moKeyField.getValue()[0], moTextValueText.getValue(), moRegistry.getPkValueId());

                        if (count > 0) {
                            validation.setMessage("¡El valor '" + moTextValueText.getValue() + "', para el campo '" + moField.getName() + "', ya existe en el catálogo!");
                            validation.setComponent(moTextValueText);
                        }
                        else {
                            String similarValues = "";
                            ArrayList<SDbValue> values = SDbValue.retrieveSimilars(miClient.getSession(), moKeyField.getValue()[0], moTextValueText.getValue(), moRegistry.getPkValueId());
                            
                            if (!values.isEmpty()) {
                                int i = 0;
                                for (SDbValue value : values) {
                                    similarValues += "\n" + ++i + ". " + value.getValueText() + (value.isDeleted() ? " (eliminado)" : "");
                                }
                                
                                String confirm = "Se " + (values.size() == 1 ? "encontró 1 valor similar" : "encontraron " + values.size() + " valores similares")
                                        + " a '" + moTextValueText.getValue() + "', para el campo '" + moField.getName() + "':";
                                
                                if (miClient.showMsgBoxConfirm(confirm + similarValues + "\n"
                                        + "¡Revise cuidadosamente para evitar crear valores duplicados!\n"
                                        + "¿Está seguro de que el valor '" + moTextValueText.getValue() + "' es realmente diferente?") != JOptionPane.YES_OPTION) {
                                    validation.setMessage("Favor de asegurarse de que el valor '" + moTextValueText.getValue() + "' sea en realidad diferente a:" + similarValues + "\n"
                                            + "En caso contrario, evite crear valores duplicados para el campo '" + moField.getName() + "'\n"
                                            + "Corrija el valor '" + moTextValueText.getValue() + "' o dé clic en '" + jbCancel.getText() + "' para abortar la captura.");
                                    validation.setComponent(moTextValueText);
                                }
                            }
                        }
                    }

                    if (validation.isValid()) {
                        if (moTextScopeInputCategories.isEditable() && !moTextScopeInputCategories.getValue().isEmpty()) {
                            component = moTextScopeInputCategories;

                            // an exception will be thrown on validation failure:
                            SDbValue.validateDataIds(miClient.getSession(), SModConsts.SU_INP_CT, moTextScopeInputCategories.getValue());
                        }

                        if (moTextScopeItems.isEditable() && !moTextScopeItems.getValue().isEmpty()) {
                            component = moTextScopeItems;

                            // an exception will be thrown on validation failure:
                            SDbValue.validateDataIds(miClient.getSession(), SModConsts.SU_ITEM, moTextScopeItems.getValue());
                        }
                    }
                }
            }
            catch (Exception e) {
                validation.setMessage(e.getMessage());
                validation.setComponent(component);
            }
        }

        return validation;
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton) {
            JButton button = (JButton) e.getSource();
            
            if (button == jbViewFieldSettings) {
                actionPerformedViewFieldSettings();
            }
            else if (button == jbEditValueText) {
                actionPerformedEditValueText();
            }
            else if (button == jbEditScopeInputCategories) {
                actionPerformedEditScopeInputCategories();
            }
            else if (button == jbEditScopeItems) {
                actionPerformedEditScopeItems();
            }
        }
    }

    @Override
    public void itemStateChanged(ItemEvent e) {
        if (e.getSource() instanceof SBeanFieldKey && e.getStateChange() == ItemEvent.SELECTED) {
            SBeanFieldKey field = (SBeanFieldKey) e.getSource();
            
            if (field == moKeyField) {
                itemStateChangedField();
            }
        }
    }
